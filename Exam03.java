
public class Exam03 {

	public static void main(String[] args) {
		/*
		 * 연산자 종류
		 * 1. 단항 연산자
		 * 	1) 증감 연산자
		 * 2. 이항 연산자
		 * 	1) 대입 연산자
		 *  2) 산술 연산자 : +,-,*,/,%
		 *  3)
		 * 
		 * 3. 삼항 연산자
		 * 
		 * */
		
		int num1 = 7;
		int num2 = 3;
		double num3 = 3.0;
		
		System.out.println(num1+num2);
		System.out.println(num1-num2);
		System.out.println(num1*num2);
		System.out.println(num1/num2);
		System.out.println(num1%num2);
		System.out.println(num1/num3);
		System.out.println(num1%num3);
		
		
		

	}

}
